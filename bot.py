# ── Data classes ──────────────────────────────────────────────────────────────

@dataclass
class RollSpec:
    num_dice: int
    sides:    int
    sel_mode: str | None    # 'kh' | 'kl' | 'dh' | 'dl' | None
    sel_n:    int | None
    modifier: int
    expr:     str           # normalized label, e.g. "4d6dl1+2"


@dataclass
class RollResult:
    all_rolls: list[int]
    kept:      list[int]    # dice that count toward the total
    dropped:   list[int]    # dice that were set aside
    subtotal:  int          # sum(kept)
    total:     int          # subtotal + modifier


# ── Utility helpers ───────────────────────────────────────────────────────────

def _get_history(uid: int) -> deque:
    """Return the user's history deque, creating it if needed.

    Moves uid to the most-recently-used position (end of OrderedDict) and evicts
    the least-recently-used entry when the dict exceeds MAX_HISTORY_USERS.
    The freshly-accessed uid is always at the end, so popitem(last=False) can
    never evict it.
    """
    if uid not in user_history:
        user_history[uid] = deque(maxlen=HISTORY_MAXLEN)
    user_history.move_to_end(uid)
    while len(user_history) > MAX_HISTORY_USERS:
        user_history.popitem(last=False)
    return user_history[uid]


# ── History persistence ───────────────────────────────────────────────────────

def _load_history() -> None:
    """Load user_history from HISTORY_FILE at startup.

    Missing file → start with empty history (normal on first run).
    Corrupted file → log the error and start fresh without crashing.
    Entries beyond HISTORY_MAXLEN are silently trimmed.
    Non-integer keys and non-string entries are skipped defensively.
    """
    if not HISTORY_FILE.exists():
        return
    try:
        raw = json.loads(HISTORY_FILE.read_text(encoding="utf-8"))
        for uid_str, entries in raw.items():
            if not isinstance(uid_str, str) or not uid_str.isdigit():
                continue
            uid = int(uid_str)
            dq: deque = deque(maxlen=HISTORY_MAXLEN)
            for entry in entries:
                if isinstance(entry, str):
                    dq.append(entry)
            if dq:
                user_history[uid] = dq
        logger.info("History loaded: %d user(s) from %s", len(user_history), HISTORY_FILE)
    except json.JSONDecodeError as exc:
        logger.error("History file corrupted, starting with empty history: %s", exc)
    except Exception as exc:
        logger.error("Could not load history from %s: %s", HISTORY_FILE, exc)


def _save_history_to_disk(snapshot: dict) -> None:
    """Write a pre-built snapshot atomically to HISTORY_FILE.

    Strategy: write to a .tmp sidecar first, then os.replace() which is
    atomic on POSIX (rename syscall) and on Windows when src/dst share the
    same drive. A crash mid-write at worst leaves a stale .tmp file; the
    main file is never partially written or truncated.
    """
    tmp = HISTORY_FILE.with_suffix(".tmp")
    try:
        tmp.write_text(json.dumps(snapshot, ensure_ascii=False), encoding="utf-8")
        os.replace(tmp, HISTORY_FILE)
    except Exception as exc:
        logger.error("Failed to save history: %s", exc)
        with contextlib.suppress(Exception):
            tmp.unlink()


async def _do_save(snapshot: dict) -> None:
    """Run the blocking disk write in a thread-pool executor."""
    loop = asyncio.get_event_loop()
    await loop.run_in_executor(None, _save_history_to_disk, snapshot)


def _schedule_history_save() -> None:
    """Snapshot user_history and schedule an async disk write. Non-blocking.

    The snapshot is built synchronously (no races with the asyncio event
    loop) and then handed off to a background Task so the caller returns
    immediately without waiting for disk I/O.
    """
    snapshot = {str(uid): list(dq) for uid, dq in user_history.items()}
    asyncio.create_task(_do_save(snapshot))


def _format_uptime() -> str:
    """Return a human-readable uptime string (hours + minutes, or minutes + seconds)."""
    if BOT_START_TIME is None:
        return "desconocido"
    delta = datetime.now() - BOT_START_TIME
    total_s = int(delta.total_seconds())
    hours, rem = divmod(total_s, 3600)
    minutes, seconds = divmod(rem, 60)
    if hours > 0:
        return f"{hours}h {minutes}m"
    return f"{minutes}m {seconds}s"


def _truncate_dice_list(rolls: list[int], sep: str = " · ") -> str:
    """Show up to MAX_DISPLAY_DICE values; summarize the rest to keep messages short."""
    if len(rolls) <= MAX_DISPLAY_DICE:
        return sep.join(str(r) for r in rolls)
    shown = sep.join(str(r) for r in rolls[:MAX_DISPLAY_DICE])
    return f"{shown} … (+{len(rolls) - MAX_DISPLAY_DICE} más)"


def _roll_annotation(spec: RollSpec, result: RollResult) -> str:
    """Return a celebration/warning string for notable single-die plain rolls."""
    if spec.num_dice == 1 and spec.sel_mode is None and spec.modifier == 0:
        if result.total == spec.sides:
            return " 🎯 *¡Crítico!*"
        if result.total == 1:
            return " 💀 *¡Pifia!*"
    return ""


async def safe_reply(message, text: str, **kwargs) -> None:
    """Send a reply with a hard length cap and TelegramError handling.

    Prevents BadRequest crashes when dice expressions produce very long output.
    If the first attempt fails (e.g. malformed Markdown), retries without
    parse_mode so the user still receives the message as plain text.
    """
    if len(text) > MAX_MSG_LEN:
        text = (
            text[:MAX_MSG_LEN - 100]
            + "\n\n_(Respuesta truncada — usa menos dados para ver el detalle completo)_"
        )
    try:
        await message.reply_text(text, **kwargs)
    except TelegramError as exc:
        logger.error("Failed to send reply: %s", exc)
        if "parse_mode" in kwargs:
            fallback = {k: v for k, v in kwargs.items() if k not in ("parse_mode", "reply_markup")}
            try:
                await message.reply_text(text, **fallback)
            except TelegramError:
                pass


