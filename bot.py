def _parse_admin_ids() -> frozenset[int]:
    """Parse ADMIN_USER_IDS from env. Ignores empty, whitespace-only, or non-numeric values."""
    raw = os.environ.get("ADMIN_USER_IDS", "")
    ids: set[int] = set()
    for part in raw.split(","):
        part = part.strip()
        if part.isdigit():
            ids.add(int(part))
    return frozenset(ids)

